package Lessons;

public class Control_Structure {

	public static void main(String[] args) {

		int Even_Odd=2;
		
		if(Even_Odd==1) {
			
			System.out.println("Today is even day.");
			
		}
		
		else if(Even_Odd==2) {
			
			System.out.println("Today is odd day.");
		}
		
		else{
			
			System.out.println("Today is free day for not only even but also odd.");
			
		}
	}

}
