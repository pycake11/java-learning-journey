package Lessons;

public class WordCase {

	public static void main(String[] args) {
		
		String text="This     is Upper Case Letter.";
		
		String textt="This is Lower Case Letter";
		
		
		System.out.println(textt.length());
		
		
		System.out.println(text.length());
		
		System.out.println(text.toUpperCase());
		
		System.out.println(textt.toLowerCase());
		
		System.out.println(text.indexOf("Upper"));
		
		System.out.println(text.charAt(0));
		
		

	}

}
