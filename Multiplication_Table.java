package Lesson6;

public class Multiplication_Table {

	public static void main(String[] args) {

		for (int i = 1; i <= 5; i++) {
			for (int j = 1; j <= 5; j++) {
				int result = i * j;
				if (result % 2 == 0) {
					System.out.print(result+"  ");
				}else {
				      
				      continue;
				      
				      
				}
				
				
			}
		
			
			
		}
		
		System.out.println();
		

	}

}
